const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      const idx = Array.from(entry.target.parentElement.children).indexOf(entry.target)
      setTimeout(() => entry.target.classList.add('visible'), Math.min(idx, 6) * 80)
      revealObserver.unobserve(entry.target)
    }
  })
}, { threshold: 0.15 })

document.querySelectorAll('.reveal').forEach(el => revealObserver.observe(el))

// ===== Counter animation =====
const counterObserver = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (!entry.isIntersecting) return
    const el = entry.target
    const target = parseInt(el.dataset.count, 10)
    const suffix = el.dataset.suffix || ''
    const dur = 1600
    const start = performance.now()
    const tick = (now) => {
      const p = Math.min((now - start) / dur, 1)
      const eased = 1 - Math.pow(1 - p, 3)
      el.textContent = Math.round(eased * target) + suffix
      if (p < 1) requestAnimationFrame(tick)
    }
    requestAnimationFrame(tick)
    counterObserver.unobserve(el)
  })
}, { threshold: 0.4 })

document.querySelectorAll('.stat-num').forEach(el => counterObserver.observe(el))

// ===== Particle mesh canvas =====
const canvas = document.getElementById('meshCanvas')
const ctx = canvas.getContext('2d')
let W, H, particles, mouse = { x: -9999, y: -9999 }

function resize() {
  W = canvas.width = canvas.offsetWidth
  H = canvas.height = canvas.offsetHeight
  const count = Math.min(90, Math.floor((W * H) / 14000))
  particles = Array.from({ length: count }, () => ({
    x: Math.random() * W,
    y: Math.random() * H,
    vx: (Math.random() - 0.5) * 0.3,
    vy: (Math.random() - 0.5) * 0.3,
    r: Math.random() * 1.6 + 0.6,
  }))
}
resize()
window.addEventListener('resize', resize)

let t = 0
function drawMesh() {
  ctx.clearRect(0, 0, W, H)
  t += 0.008
  const maxDist = 130
  for (let i = 0; i < particles.length; i++) {
    const p = particles[i]
    const wave = Math.sin((p.x * 0.01) + t) * 0.4 + Math.cos((p.y * 0.012) + t * 0.8) * 0.4
    p.x += p.vx + wave * 0.15
    p.y += p.vy + wave * 0.1
    if (p.x < 0 || p.x > W) p.vx *= -1
    if (p.y < 0 || p.y > H) p.vy *= -1
    const dx = p.x - mouse.x, dy = p.y - mouse.y
    const md = Math.hypot(dx, dy)
    if (md < 120) { p.x += dx / md * 1.5; p.y += dy / md * 1.5 }
    ctx.beginPath()
    ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2)
    ctx.fillStyle = 'rgba(0, 245, 212, 0.8)'
    ctx.fill()
    for (let j = i + 1; j < particles.length; j++) {
      const q = particles[j]
      const d = Math.hypot(p.x - q.x, p.y - q.y)
      if (d < maxDist) {
        const a = (1 - d / maxDist) * 0.35
        ctx.strokeStyle = `rgba(0, 212, 255, ${a})`
        ctx.lineWidth = 0.6
        ctx.beginPath()
        ctx.moveTo(p.x, p.y)
        ctx.lineTo(q.x, q.y)
        ctx.stroke()
      }
    }
  }
  requestAnimationFrame(drawMesh)
}
drawMesh()

